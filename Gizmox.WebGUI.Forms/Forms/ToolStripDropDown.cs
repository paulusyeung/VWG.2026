        this.AutoScroll = true;
        this.ScrollerType = ScrollerType.Arrows;
        this.VScroll = true;
      }

      /// <summary>Gets or sets the control custom style.</summary>
      public override string CustomStyle
      {
        get => nameof (ToolStripDropDownContentPanel);
        set => base.CustomStyle = value;
      }

      /// <summary>Gets or sets the owner tool strip drop down.</summary>
      /// <value>The owner tool strip drop down.</value>
      internal ToolStripDropDown OwnerToolStripDropDown
      {
        get => this.mobjOwnerToolStripDropDown;
        set => this.mobjOwnerToolStripDropDown = value;
      }

      /// <summary>Renders the controls sub tree</summary>
      /// <param name="objContext"></param>
      /// <param name="objWriter"></param>
      /// <param name="lngRequestID"></param>
      protected override void RenderControls(
        IContext objContext,
        IResponseWriter objWriter,
        long lngRequestID)
      {
        if (this.mobjOwnerToolStripDropDown == null)
          return;
        ToolStripItemCollection items = this.mobjOwnerToolStripDropDown.Items;
        if (items == null)
          return;
        foreach (ToolStripItem toolStripItem in (ArrangedElementCollection) items)
        {
          if (toolStripItem.Visible)
            toolStripItem.RenderToolStripItem(objContext, objWriter, lngRequestID);
        }
      }

      /// <summary>Get maximum width of image over all items</summary>
      /// <returns></returns>
      internal int GetContentImageMaxWidth()
      {
        int contentImageMaxWidth = 0;
        if (this.mobjOwnerToolStripDropDown != null)
        {
          Size size = this.mobjOwnerToolStripDropDown.ImageScalingSize;
          contentImageMaxWidth = size.Width;
          ToolStripItemCollection items = this.mobjOwnerToolStripDropDown.Items;
          if (items != null)
          {
            contentImageMaxWidth = 0;
            foreach (ToolStripItem toolStripItem in (ArrangedElementCollection) items)
            {
              if (toolStripItem.Visible)
              {
                int width;
                if (!toolStripItem.AutoSize)
                {
                  size = this.mobjOwnerToolStripDropDown.ImageScalingSize;
                  width = size.Width;
                }
                else
                {
                  size = toolStripItem.GetPreferredSizeByImage(Size.Empty);
                  width = size.Width;
                }
                int num = width;
                if (num > contentImageMaxWidth)
                  contentImageMaxWidth = num;
              }
            }
          }
        }
        return contentImageMaxWidth;
      }

      /// <summary>Gets the size of the content preffered.</summary>
      /// <returns></returns>
      internal Size GetContentPrefferedSize()
      {
        int width = 0;
        int height = 0;
        ToolStripMenuItemSkin skin = SkinFactory.GetSkin((ISkinable) this, typeof (ToolStripMenuItemSkin)) as ToolStripMenuItemSkin;
        if (this.mobjOwnerToolStripDropDown != null)
        {
          ToolStripItemCollection items = this.mobjOwnerToolStripDropDown.Items;
          if (items != null)
          {
            int num1 = 0;
            foreach (ToolStripItem toolStripItem in (ArrangedElementCollection) items)
            {
              if (toolStripItem.Visible)
              {
                Size size = toolStripItem.AutoSize ? toolStripItem.GetPreferredSize(Size.Empty) : toolStripItem.Size;
                if (size.Width > num1)
                  num1 = size.Width;
                height += size.Height;
              }
            }
            width += num1;
            if (skin != null)
            {
              int num2 = this.Context == null ? 0 : (this.Context.RightToLeft ? 1 : 0);
              int num3 = (num2 == 0 ? width + skin.MenuItemImageWidthLTR : width + skin.MenuItemImageWidthRTL) + skin.TextImageGapSize + skin.TextArrowGapSize;
              width = num2 == 0 ? num3 + skin.MenuItemArrowImageWidthLTR : num3 + skin.MenuItemArrowImageWidthRTL;
            }
          }
        }
        if (skin != null)
          width += skin.MenuItemImageExtraWidth;
        return new Size(width, height);
      }
    }

