        this.InitializePrefferedSize();
      }

      /// <summary>Initializes a preffered size.</summary>
      private void InitializePrefferedSize()
      {
        int width = 0;
        int height = 0;
        if (SkinFactory.GetSkin((ISkinable) this, typeof (ToolStripDropDownSkin)) is ToolStripDropDownSkin skin)
        {
          height += skin.DropDownOffsetHeight;
          width += skin.DropDownOffsetWidth;
        }
        if (this.mobjToolStripDropDownContentPanel != null)
        {
          Size contentPrefferedSize = this.mobjToolStripDropDownContentPanel.GetContentPrefferedSize();
          width += contentPrefferedSize.Width;
          height += contentPrefferedSize.Height;
        }
        if (this.mobjOwnerToolStripDropDown != null && this.mobjOwnerToolStripDropDown.HeaderPanel != null)
        {
          switch (this.mobjOwnerToolStripDropDown.HeaderPanel.Dock)
          {
            case DockStyle.Top:
            case DockStyle.Bottom:
              height += this.mobjOwnerToolStripDropDown.HeaderPanel.Height;
              break;
            case DockStyle.Right:
            case DockStyle.Left:
              width += this.mobjOwnerToolStripDropDown.HeaderPanel.Width;
              break;
          }
        }
        if (height <= 0 || width <= 0)
          return;
        this.Size = new Size(width, height);
      }

      /// <summary>Renders the image size attributes.</summary>
      /// <param name="objWriter">The writer.</param>
      /// 
      ///             /// <param name="blnForceRender">if set to <c>true</c> [BLN force render].</param>
      private void RenderImageSizeAttributes(IAttributeWriter objWriter)
      {
        if (this.mobjOwnerToolStripDropDown == null)
          return;
        Size imageScalingSize = this.mobjOwnerToolStripDropDown.ImageScalingSize;
        objWriter.WriteAttributeString("IH", imageScalingSize.Height.ToString((IFormatProvider) CultureInfo.InvariantCulture));
        objWriter.WriteAttributeString("IW", imageScalingSize.Width.ToString((IFormatProvider) CultureInfo.InvariantCulture));
        objWriter.WriteAttributeString("MIMW", this.mobjToolStripDropDownContentPanel.GetContentImageMaxWidth().ToString((IFormatProvider) CultureInfo.InvariantCulture));
      }

      /// <summary>Renders the forms attribute</summary>
      /// <param name="objContext"></param>
      /// <param name="objWriter"></param>
      protected override void RenderAttributes(IContext objContext, IAttributeWriter objWriter)
      {
        base.RenderAttributes(objContext, objWriter);
        this.RenderImageSizeAttributes(objWriter);
        ToolStripItem ownerToolStripItem = this.OwnerToolStripItem;
        if (ownerToolStripItem != null)
        {
          IAttributeWriter attributeWriter = objWriter;
          long num = ownerToolStripItem.OwnerID;
          string str1 = num.ToString();
          num = ownerToolStripItem.MemberID;
          string str2 = num.ToString();
          string strValue = string.Format("{0}_{1}", (object) str1, (object) str2);
          attributeWriter.WriteAttributeString("oId", strValue);
          if (ownerToolStripItem.Owner is ToolStripDropDown && ownerToolStripItem is ToolStripDropDownItem stripDropDownItem && stripDropDownItem.DropDown.Items.Count > 0)
            objWriter.WriteAttributeString("SF", "1");
        }
        objWriter.WriteAttributeString("TSDD", "1");
        if (this.mobjOwnerToolStripDropDown.AutoClose)
          return;
        objWriter.WriteAttributeString("ACDD", "0");
      }

      /// <summary>Fires an event.</summary>
      /// <param name="objEvent">event.</param>
      protected override void FireEvent(IEvent objEvent)
      {
        ToolStripItem toolStripItem = (ToolStripItem) null;
        if (this.mobjOwnerToolStripDropDown != null)
        {
          string member = objEvent.Member;
          if (!string.IsNullOrEmpty(member))
          {
            int result = -1;
            if (int.TryParse(member, out result))
              toolStripItem = this.mobjOwnerToolStripDropDown.Items[result - 1];
          }
        }
        if (toolStripItem != null)
          toolStripItem.FireToolStripItemEvent(objEvent);
        else
          base.FireEvent(objEvent);
      }

