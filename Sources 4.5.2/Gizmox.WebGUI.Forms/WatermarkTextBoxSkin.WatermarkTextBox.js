/// <method name="WatermarkTextBox_Focus">
/// <summary>
///
/// </summary>
/// <param name="strGuid">TextBox guid.</param>
/// <param name="objElement">The input control value</param>
/// <param name="objWindow">The text box window</param>
function WatermarkTextBox_Focus(strGuid, objElement, objWindow)
{
    if (Web_IsAttribute(objElement, "vwgiswatermark", "1", true)) {
        var strTheme = Web_GetThemeName(objElement);
        var strWatermarkSinglelineClass = mobjApp.Web_GetAttribute(objElement, "vwgwatermarksinglelineclass");
        var strWatermarkTextClass = mobjApp.Web_GetAttribute(objElement, "vwgwatermarktextclass");
        var strWatermarkMultilineClass = mobjApp.Web_GetAttribute(objElement, "vwgwatermarkmultilineclass");

        if (Data_IsAttribute(strGuid, "Attr.Multiline", "1")) {
            objElement.className = strTheme + " TextBox-Input " + strWatermarkMultilineClass;
            objElement.value = "";
            mobjApp.Web_SetAttribute(objElement, 'vwgiswatermark', "0");
        }
        else {
            objElement.value = "";
            mobjApp.Web_SetAttribute(objElement, 'vwgiswatermark', "0");
            objElement.className = strTheme + " Common-FontData TextBox-Input " + strWatermarkSinglelineClass;

            // For IE8, need to set asynchronously, else focus gets lost
            if (mcntIsObsoleteIE) {
                setTimeout(function () {
                    WatermarkTextBox_ChangeInputType(strGuid, WatermarkTextBox_GetTargetType(strGuid, objWindow), true);
                }, 10);
            }
            else {
                WatermarkTextBox_ChangeInputType(strGuid, WatermarkTextBox_GetTargetType(strGuid, objWindow), true);
            }
        }

    }
}
/// </method>

/// <method name="WatermarkTextBox_Blur">
/// <summary>
///
/// </summary>
/// <param name="strGuid">TextBox guid.</param>
/// <param name="objElement">The input control value</param>
/// <param name="objWindow">The text box window</param>
/// <param name="blnIsEnter">The event was generated by enter key</param>
function WatermarkTextBox_Blur(strGuid, objElement, objWindow)
{
    // Check if the watermark textbox is in edit mode
    if (Web_IsAttribute(objElement, "vwgiswatermark", "0", true)) {
        if (objElement.value == "") {
            var strTheme = Web_GetThemeName(objElement);
            var strWatermarkSinglelineClass = mobjApp.Web_GetAttribute(objElement, "vwgwatermarksinglelineclass");
            var strWatermarkTextClass = mobjApp.Web_GetAttribute(objElement, "vwgwatermarktextclass");
            var strWatermarkMultilineClass = mobjApp.Web_GetAttribute(objElement, "vwgwatermarkmultilineclass");

            if (Data_IsAttribute(strGuid, "Attr.Multiline", "1")) {
                objElement.className = strTheme + " TextBox-Input " + strWatermarkMultilineClass + " " + strWatermarkTextClass;
                objElement.value = Aux_DecodeText(Data_GetAttribute(strGuid, "Attr.Message", ""));
                mobjApp.Web_SetAttribute(objElement, 'vwgiswatermark', "1");
            }
            else {
                objElement.value = Aux_DecodeText(Data_GetAttribute(strGuid, "Attr.Message", ""));
                mobjApp.Web_SetAttribute(objElement, 'vwgiswatermark', "1");
                objElement.className = strTheme + " Common-FontData TextBox-Input " + strWatermarkSinglelineClass + " " + strWatermarkTextClass;

                var strType = WatermarkTextBox_GetTargetType(strGuid, objWindow);
                strType = (strType === 'password' ? "text" : strType);
                // For IE8, need to set asynchronously, else focus gets lost
                if (mcntIsObsoleteIE) {
                    setTimeout(function ()
                    {
                        WatermarkTextBox_ChangeInputType(strGuid, strType, false);
                    }, 10);
                }
                else {
                    objElement = WatermarkTextBox_ChangeInputType(strGuid, strType, false);
                }

            }

            Common_TextChange(strGuid, "", objWindow);
        }
        else {
            Common_TextChange(strGuid, objElement.value, objWindow);
        }
    }
}
