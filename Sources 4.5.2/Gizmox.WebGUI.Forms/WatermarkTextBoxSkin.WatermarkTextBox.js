/// <method name="WatermarkTextBox_ChangeInputType">
/// <summary>
/// Change input type between text/password for watermarktextbox.
/// For IE8, input type can not be changed on a node already in document, needs special handling
/// </summary>
/// <param name="strGuid">The VWG Id of the element</param>
/// <param name="strTargetType">The new input type to set</param>
/// <param name="blnNeedsFocusing">True </param>
function WatermarkTextBox_ChangeInputType(strGuid, strTargetType, blnNeedsFocusing) {
    // Find the input node
    objElement = Web_GetTargetElementByDataId(strGuid);
    if (objElement) {
        // If target type is equal to current, no need to change
        if (objElement.type == strTargetType)
            return objElement;

        // Only for IE8
        if (mcntIsObsoleteIE) {
            //copy the element itself, its html source, and value text to a variable
            var oldHtml = objElement.outerHTML;
            var text = objElement.value;
            //save a few VWG specific extended attributes
            var strWatermarkSinglelineClass = Web_GetAttribute(objElement, "vwgwatermarksinglelineclass");
            var strWatermarkTextClass = Web_GetAttribute(objElement, "vwgwatermarktextclass");
            var strWatermarkMultilineClass = Web_GetAttribute(objElement, "vwgwatermarkmultilineclass");

            if (strTargetType == "text") {
                //replace "password" with "text" in the html if it is a password field
                var newHtml = oldHtml.replace(/password/g, "text");
            }
            else {
                //replace "text" with "password" if it is a text field
                newHtml = oldHtml.replace(/text/g, "password");
            }

            //update the html on the node
            objElement.outerHTML = newHtml;
            // DOM has changed, find node again
            objElement = Web_GetTargetElementByDataId(strGuid);
            //restore the text value
            objElement.value = text;
            //restore VWG extended attributes
            Web_SetAttribute(objElement, "vwgwatermarksinglelineclass", strWatermarkSinglelineClass);
            Web_SetAttribute(objElement, "vwgwatermarktextclass", strWatermarkTextClass);
            Web_SetAttribute(objElement, "vwgwatermarkmultilineclass", strWatermarkMultilineClass);

            // If node is replaced and we are within onFocus event handler, re-focus right after we finish
            if (blnNeedsFocusing) {
                setTimeout(function () {
                    objElement.focus();
                }, 10);
            }

            // return new node
            return objElement;
        }
        else {
            // All browsers, except IE8, support direct and dynamic change of input type
            objElement.type = strTargetType;
            return objElement;
        }
    }
}
/// </method>


/// <method name="WatermarkTextBox_GetTargetType">
/// <summary>
///
/// </summary>
function WatermarkTextBox_GetTargetType(strID, objWindow)
{
    // Target-type attribute is on the VWG element itself
    var strType = 'text';
    var objControl = Web_GetElementByDataId(strID, objWindow);
    if (objControl)
    {
        strType = Web_GetAttribute(objControl, "vwgtargettype");
    }

    return strType;
}
