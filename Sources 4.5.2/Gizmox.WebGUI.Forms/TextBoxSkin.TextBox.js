/// <method name="TextBox_IsValidCharacter">
/// <summary>
/// Checks if a character is valid.
/// </summary>
function TextBox_IsValidCharacter(intKeyCode,strCharacterValidationMask,strCharacterValidationExpression,objWindow,objInput,objEvent)
{
    // Define a casing indicator.
    var blnValidCharacter = true;
    
    // Get char value as string.
    var strValue  = String.fromCharCode(intKeyCode);
              
    // Check mask.
    if (strCharacterValidationMask!="")
    { 		          
        // Build a RegExp object as for the textbox's mask.
        var objRegExp = new RegExp("[" +strCharacterValidationMask+ "]","gm");
		
	    // Check mask matching.
        blnValidCharacter = objRegExp.test(strValue);
    }
    
    if(blnValidCharacter && strCharacterValidationExpression!="")
    {
       blnValidCharacter = Web_ValidateValue(strCharacterValidationExpression,strValue,objInput,objWindow,objEvent);
    }
    
    return blnValidCharacter;
}
/// </method>

