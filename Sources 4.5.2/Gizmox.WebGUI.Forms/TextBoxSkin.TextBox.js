function TextBox_IsMultiline(strGuid)
{
    var blnIsMultiline = false;
    
    if(strGuid && strGuid!="")
    {
        // Get text box node
	    var objNode = Data_GetNode(strGuid);
	    if(objNode)
	    {
	        blnIsMultiline = Xml_IsAttribute(objNode,"Attr.Multiline","1");
	    }
    }
    
    return blnIsMultiline;
}



/// <method name="TextBox_KeyPress">
/// <summary>
/// TextBox_KeyPress - Handles onkeypress event for all types of keys
/// </summary>
function TextBox_KeyPress(strGuid,strCharacterValidationMask,strCharacterValidationExpression,objWindow,objInput,objEvent)
{
    // Get the pressed key
    var intKeyCode = Web_GetEventKeyCode(objEvent)*1;

    // Check if enter was pressed.
    if (intKeyCode==mcntEnterKey && !TextBox_IsMultiline(strGuid))
    {
        Common_TextChange(strGuid,objInput.value,objWindow,true);
    }
    
    // Check if a character was pressed and that the control key is not held.
    else if(Web_IsCharacterPressed(objEvent) && !Web_IsControl(objEvent))
    {
        // Validate character.
        if(!TextBox_IsValidCharacter(intKeyCode,strCharacterValidationMask,strCharacterValidationExpression,objWindow,objInput,objEvent))
        {
	        // Cancel last key press.
            Web_EventCancelBubble(objEvent);
        }
    }

    // check if key events sync is needed
    Common_TextBoxSyncKeyEvent(strGuid, objWindow, objEvent, objInput, "TextBoxRealTimeKeyPress");
}
/// </method>


/// <method name="TextBox_KeyPressMultiLine">
/// <summary>
/// Handles the Key Multi line
/// </summary>
function TextBox_KeyPressMultiLine(strGuid,objTextArea,objEvent,objWindow)
{
    
    objWindow = objWindow || window;
    // Check if the pressed key is a character and the ctrl button is not pressed
    if ((Web_IsCharacterPressed(objEvent) && !Web_IsControl(objEvent)) || objEvent.keyCode == mcntEnterKey)
    {
        // Try getting selection range.
        var objSelection = Web_GetSelectionRange(objTextArea);

        // If there is no selected text
        if (!objSelection || objSelection.Start == objSelection.End)
        {
            // Gets max length attribute.
            var intMaxLength = parseInt(Data_GetAttribute(strGuid, "Attr.MaxLength", "0"), 10);
            if (intMaxLength > 0)
            {
                //If we reached the max length Stop
                if (objTextArea.value.length >= intMaxLength)
                {
                    // Cancel default behavior.
                    Web_EventCancelBubble(objEvent, true, false);
                }
            }
        }
    }

    // check if key events sync is needed
    Common_TextBoxSyncKeyEvent(strGuid, objWindow, objEvent, objTextArea, "TextBoxRealTimeKeyPress");
}
/// </method>


/// <method name="TextBox_KeyDown">
/// <summary>
/// TextBox_KeyDown - Handle onkeydown event for all types of keys
/// </summary>
function TextBox_KeyDown(strGuid,objWindow,objEvent,objElement)
{
    // Get the pressed key
	var intKeyCode = Web_GetEventKeyCode(objEvent);
    
	// Check event code
	if (intKeyCode == mcntTabKey)
	{
	    // Get data node
	    var objNode = Data_GetNode(strGuid);
	    if (objNode)
	    {
	        if (Xml_IsAttribute(objNode, "Attr.AcceptsTab", "1"))
	        {
	            // Gets max length attribute.
	            var intMaxLength = parseInt(XML_GetAttribute(objNode, "Attr.MaxLength", "-1"), 10);

	            var blnSetTab = false;

	            //If we reached the max length Stop or that there is no limit
	            if (intMaxLength == -1 || (objElement.value.length < intMaxLength))
	            {
	                blnSetTab = true;
	            }
	            else //check if some text is selected so the tab will replace it
	            {
	                var objRange = Web_GetSelectionRange(objElement);
	                if (objRange.End - objRange.Start > 0)
	                {
	                    blnSetTab = true;
	                }
	            }

	            if (blnSetTab)
	            {
	                //set the character "\t" for tab character
	                Web_SetSelectedTextValue(objEvent, objWindow, "\t");
	            }

	            // Cancel bubble
	            Web_EventCancelBubble(objEvent);
	        }
	    }
	}

    Common_TextBoxSyncKeyEvent(strGuid, objWindow, objEvent, objElement, "TextBoxRealTimeKeyDown");
}
/// </method>

