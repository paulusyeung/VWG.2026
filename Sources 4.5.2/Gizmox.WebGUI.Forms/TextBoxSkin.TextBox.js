/// <method name="TextBox_OnPasteAsync">
/// <summary>
/// 
/// </summary>
function TextBox_OnPasteAsync(strID, objTextElement, blnIsTextArea)
{
    if (objTextElement)
    {
        if (blnIsTextArea)
        {
            // Textarea does not have a max-length attribute so we need to simulate it
            var intMaxLength = parseInt(Data_GetAttribute(strID, "Attr.MaxLength", "0"), 10);

            if (intMaxLength > 0)
            {
                // Get text area value.
                var strTextAreaValue = objTextElement.value;

                // Check if text area value exceeded maximal length. 
                if (strTextAreaValue.length > intMaxLength)
                {
                    objTextElement.value = strTextAreaValue.substr(0, intMaxLength);
                }
            }
        }
    }
}
/// </method>

