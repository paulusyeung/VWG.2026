namespace Gizmox.WebGUI.Virtualization.Management
{
internal class PhysicalStorageNode : ServerExplorerNode
	{
		internal PhysicalStorageNode(string strDirectory)
		{
			base.Tag = strDirectory;
			base.Label = Path.GetFileName(strDirectory.Trim('/', '\\'));
			base.Image = new IconResourceHandle("Folder.gif");
		}

		protected override void LoadColumns(ListView.ColumnHeaderCollection objColumns)
		{
			objColumns.Add("Name", 150, Gizmox.WebGUI.Forms.HorizontalAlignment.Left);
			objColumns.Add("Size", 150, Gizmox.WebGUI.Forms.HorizontalAlignment.Left);
			objColumns.Add("Type", 150, Gizmox.WebGUI.Forms.HorizontalAlignment.Left);
			objColumns.Add("Date Modified", 150, Gizmox.WebGUI.Forms.HorizontalAlignment.Left);
		}

		protected override void LoadItems(ListView.ListViewItemCollection objItems)
		{
			if (Directory.Exists((string)base.Tag))
			{
				DirectoryInfo directoryInfo = new DirectoryInfo((string)base.Tag);
				FileInfo[] files = directoryInfo.GetFiles();
				foreach (FileInfo fileInfo in files)
				{
					ListViewItem listViewItem = objItems.Add(fileInfo.Name);
					listViewItem.SmallImage = new IconResourceHandle("File.gif");
					listViewItem.Tag = fileInfo.FullName;
					listViewItem.SubItems.Add((int)(fileInfo.Length / 1024) + "Kb");
					listViewItem.SubItems.Add(fileInfo.Extension);
					listViewItem.SubItems.Add(fileInfo.LastWriteTime.ToShortDateString());
				}
			}
		}

		protected override void LoadNodes()
		{
			if (Directory.Exists((string)base.Tag))
			{
				DirectoryInfo directoryInfo = new DirectoryInfo((string)base.Tag);
				DirectoryInfo[] directories = directoryInfo.GetDirectories();
				foreach (DirectoryInfo directoryInfo2 in directories)
				{
					base.Nodes.Add(new PhysicalStorageNode(directoryInfo2.FullName));
				}
			}
		}
	}
}
