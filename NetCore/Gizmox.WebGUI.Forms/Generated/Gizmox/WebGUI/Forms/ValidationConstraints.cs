		ImmediateChildren = 0x10,
		/// Validates all child controls, and all children of these child controls, regardless of their property settings. </summary>
		None = 0,
		/// Validates child controls that can be selected.</summary>
		Selectable = 1,
		/// Validates child controls that have a <see cref="P:System.Windows.Forms.Control.TabStop"></see> value set, which means that the user can navigate to the control using the TAB key. </summary>
		TabStop = 8,
