	[EditorBrowsable(EditorBrowsableState.Never)]
	public class TextResourceReferenceTypeConverter : TypeConverter
	{
		public override bool CanConvertFrom(ITypeDescriptorContext context, Type sourceType)
		{
			if (!(sourceType == typeof(string)))
			{
				/*OpCode not supported: LdMemberToken*/;
				return base.CanConvertFrom(context, sourceType);
			}
			return true;
		}

		public override bool CanConvertTo(ITypeDescriptorContext context, Type destinationType)
		{
			if (destinationType == typeof(string))
			{
				return true;
			}
			return base.CanConvertTo(context, destinationType);
		}

		public override object ConvertTo(ITypeDescriptorContext context, CultureInfo culture, object value, Type destinationType)
		{
			if (!(destinationType == typeof(string)))
			{
				/*OpCode not supported: LdMemberToken*/;
			}
			else
			{
				if (value is TextResourceReference textResourceReference)
				{
					return textResourceReference.ResourceData;
				}
				/*OpCode not supported: LdMemberToken*/;
			}
			return base.ConvertTo(context, culture, value, destinationType);
		}

		public override object ConvertFrom(ITypeDescriptorContext context, CultureInfo culture, object value)
		{
			if (value == null)
			{
				return TextResourceReference.Empty;
			}
			if (value is string)
			{
				if (context != null)
				{
					if (context.Instance != null)
					{
						string text = (string)value;
						if (!string.IsNullOrEmpty(text))
						{
							return new TextResourceReference(text);
						}
						return TextResourceReference.Empty;
					}
					/*OpCode not supported: LdMemberToken*/;
				}
				return TextResourceReference.Empty;
			}
			return base.ConvertFrom(context, culture, value);
		}

		private Skin GetSkinFromInstance(object objInstance)
		{
			if (!(objInstance is ISkinContainer skinContainer))
			{
				/*OpCode not supported: LdMemberToken*/;
				return objInstance as Skin;
			}
			return skinContainer.Skin;
		}

		public override bool GetStandardValuesSupported(ITypeDescriptorContext context)
		{
			return true;
		}

		public override bool GetStandardValuesExclusive(ITypeDescriptorContext context)
		{
			return true;
		}

		public override StandardValuesCollection GetStandardValues(ITypeDescriptorContext context)
		{
			List<string> list = new List<string>();
			list.Add("");
			if (context == null)
			{
				/*OpCode not supported: LdMemberToken*/;
			}
			else if (context.Instance != null)
			{
				Skin skinFromInstance = GetSkinFromInstance(context.Instance);
				if (skinFromInstance == null)
				{
					/*OpCode not supported: LdMemberToken*/;
				}
				else
				{
					Dictionary<string, SkinResource> resources = skinFromInstance.Resources;
					foreach (string key in resources.Keys)
					{
						if (!(resources[key] is TextResource))
						{
							/*OpCode not supported: LdMemberToken*/;
						}
						else
						{
							list.Add(key);
						}
					}
				}
			}
			list.Sort();
			return new StandardValuesCollection(list);
		}
	}
